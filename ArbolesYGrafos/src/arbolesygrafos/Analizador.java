    public void analizar(String cad) {
        String pal = "";
        cont=0;
        for(int a=0;a<20;a++)
            aux[a]="";
        bandera=false;
        m.setRowCount(0);
        Object O[] = new Object[2];
        cad = cad.replace(" ", "");
        for (int i = 1; i <=cad.length(); i++) {
            String c = cad.substring(i-1, i);
            if (esOp(c) == -1) {
                aux[cont]=aux[cont]+c;
                pal = pal + c;
            } else if (GrafosMio.esIde(pal)){
                O[0] = pal;
                 cont++;
                 O[1] = "Identificador";
                m.addRow(O);
                O[0] = c;
                O[1] = "Operador";
                m.addRow(O);
                pal = "";
                c = "";
            } else if (GrafosMio.esEntero(pal)) {
                O[0] = pal;
                 cont++;
                O[1] = "Entero";
                m.addRow(O);
                O[0] = c;
                O[1] = "Operador";
                m.addRow(O);
                pal = "";
                c = "";
            } else if (GrafosMio.esFloat(pal)){
                O[0] = pal;
                 cont++;
                O[1] = "Flotante";
                m.addRow(O);
                O[0] = c;
                O[1] = "Operador";
                m.addRow(O);
                pal = "";
                c = "";
            } else if (GrafosMio.esHex(pal)) {
                O[0] = pal;
                 cont++;
                O[1] = "Hexadecimal";
                m.addRow(O);
                O[0] = c;
                O[1] = "Operador";
                m.addRow(O);
                pal = "";
                c = "";
            }
        }
        verificar(cad);
    }

    private int esOp(String c) {
        for (int i = 0; i < OP.length; i++) {
            if (c.equals(OP[i])) {
                return i;
            }
        }
        return -1;
    }

